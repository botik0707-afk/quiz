# ── Запуск ─────────────────────────────────────────────────────

def main() -> None:
    app = Application.builder().token(BOT_TOKEN).build()

    conv = ConversationHandler(
        entry_points=[CommandHandler("start", start)],
        states={
            Q1: [
                CallbackQueryHandler(begin, pattern="^begin$"),
                CallbackQueryHandler(q1_answer, pattern="^h2:"),
            ],
            Q2: [CallbackQueryHandler(q2_answer, pattern="^h10:")],
            Q3: [CallbackQueryHandler(q3_answer, pattern="^h6:")],
            Q4: [CallbackQueryHandler(q4_answer, pattern="^flow:")],
            Q5: [CallbackQueryHandler(q5_answer, pattern="^sun:")],
        },
        fallbacks=[
            CommandHandler("start", start),
            CallbackQueryHandler(restart, pattern="^restart$"),
        ],
        allow_reentry=True,
    )

    app.add_handler(conv)
    app.add_handler(CallbackQueryHandler(about, pattern="^about$"))
    app.add_handler(CallbackQueryHandler(restart, pattern="^restart$"))

    print("✅ Бот запущен. Нажмите Ctrl+C для остановки.")
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
