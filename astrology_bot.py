def sign_keyboard(prefix: str) -> InlineKeyboardMarkup:
    rows = []
    for i in range(0, len(SIGNS), 3):
        row = [InlineKeyboardButton(s, callback_data=f"{prefix}:{s}") for s in SIGNS[i:i+3]]
        rows.append(row)
    return InlineKeyboardMarkup(rows)

def house_keyboard(prefix: str) -> InlineKeyboardMarkup:
    houses = [f"{i} дом" for i in range(1, 13)]
    rows = []
    for i in range(0, len(houses), 3):
        row = [InlineKeyboardButton(h, callback_data=f"{prefix}:{h}") for h in houses[i:i+3]]
        rows.append(row)
    return InlineKeyboardMarkup(rows)

